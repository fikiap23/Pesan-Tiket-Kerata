package interfaces;

/**
 * Antarmuka untuk objek pengguna.
 */
public interface UserInterface {
    /**
     * Mengembalikan username pengguna.
     *
     * @return username pengguna.
     */
    String getUsername();

    /**
     * Mengembalikan password pengguna.
     *
     * @return password pengguna.
     */
    String getPassword();

}
